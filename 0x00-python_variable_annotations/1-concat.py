#!/usr/bin/env python3
"""
Concat function module
"""


def concat(str1: str, str2: str) -> str:
    """
    Type-annotated concatenation function

    Args:
        str1 (str) - first string
        str2 (str) - second string

    Returns:
        Result of concatenating str2 to str1
    """
    return str1 + str2
